package bars;

public class DataTypes {

	public static void main(String[] args) {
		int a = 'A';
		char c = 'A';
		char d = 35;
		
		int i1 = 10;
		int i2 = 20;
		int i3 = 30;
		
		System.out.println(c);
		System.out.println(a);
		System.out.println((char) a);
		System.out.println(d);
		
		System.out.println(i1+i2+i3);
		
		char a1 = 'a';
		char a2 = '\u0001';
		
		System.out.println((char)(a1+a2));
		
	}	
}
