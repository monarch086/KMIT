package home;

public class Student {

	private String name;
	private int subject_java;
	private int subject_math;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSubject_java() {
		return subject_java;
	}
	public void setSubject_java(int subject_java) {
		this.subject_java = subject_java;
	}
	public int getSubject_math() {
		return subject_math;
	}
	public void setSubject_math(int subject_math) {
		this.subject_math = subject_math;
	}
	
}
